#include <iostream>
#include <cstdio>
using namespace std;

int n;
int a[120000],v[120000];

int main()
{
    while (scanf("%d",&n) != EOF)
    {
        for (int i = 1;i <= n;i++)
            scanf("%d",&a[i]);
        for (int j = 1;j <= n;j++)
            scanf("%d",&v[i]);

    }
}
