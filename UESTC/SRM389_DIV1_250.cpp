class ApproximateDivision {
public:
	double quotient(int, int, int);
};

double ApproximateDivision::quotient(int a, int b, int terms) {
    double res = 0;
    double tres;
    for (int t = 1;;t *= 2)
    if (t >= b)
    {
        tres = 0;
        double _c,_t;
        _c = 1.0;
        _t = t;
        for (int i = 1;i <= terms;i++)
        {
            tres += _c/_t;
            _c *= (t-b);
            _t *= t;
        }
        return tres*a;
    }
}

