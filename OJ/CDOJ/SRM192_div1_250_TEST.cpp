class DigitMultiples {
public:
	int getLongest(string, string);
};

bool can(string a, string b)
{
     int k,i;
     for (k = 0;k < 10;k++)
     {
         for (i = 0;i < a.size();i++)
             if ((a[i]-'0')*k != (b[i]-'0')) return false;
         return true;
     }
     return false;
}
 
int DigitMultiples::getLongest(string single, string multiple) 
{
    int k,i,j,l1,l2,l;
    l1 = single.size();
    l2 = multiple.size();
    l = 0;
    for (i = 0;i < l1;i++)
    for (j = 0;j < l2;j++)
    for (k = l+1;i+l < l1 && j+l < l2;k++)
    if (can(single.substr(i,k),multiple.substr(j,k)) == true)  l = k;
    return l;
}

