#include <iostream>
#include <cstdio>
#include <cstring>
#include <algorithm>
using namespace std;

int n,m,a,b,res;

int main()
{
    while (scanf("%d%d",&n,&m) != EOF)
    {
        for (int i = 1;i <= m;i++)
            scanf("%d%d",&a,&b);
        res = 0;
        printf("%d\n",res);
    }
    return 0;
}
