class SubstitutionCode {
public:
	int getValue(string, string);
};

int SubstitutionCode::getValue(string key, string code) {
	int l1,l2,i,j,res;
	l1 = key.size();
	l2 = code.size();
	res = 0;
	for (i = 0;i < l2;i++)
    for (j = 0;j < l1;j++)
    if (key[j] == code[i])
    {
                   res = res*10+(j+1)%10;
                   break;
    }
    return res;
}

