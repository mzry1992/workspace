class MissingParentheses {
public:
	int countCorrections(string);
};

int s[50],top;

int MissingParentheses::countCorrections(string par) {
    int n = par.size();
    top = 0;
    for (int i = 0;i < n;i++)
        if (par[i] == '(')
            s[top++] = 0;
        else
        {
            s[top++] = 1;
            if (top > 1)
                if (s[top-2] == 0)
                    top -= 2;
        }
    return top;
}

