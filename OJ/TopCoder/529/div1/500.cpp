class MinskyMystery {
public:
	int computeAnswer(long long);
};

const long long mod = 1000000009LL;

long long Gao(long long n,long long b)
{
    long long q = n/b;
    if (n%b == 0)
        return ((2*b+1+b)*q)%mod;
    else
    {
        long long tmp = (2*b+1+b)*q+(n-q*b)*3+1+n+1;
        return (tmp+Gao(n,b+1))%mod;
    }
}

int MinskyMystery::computeAnswer(long long N) {
    if (N < 2)  return -1;
    return (Gao(N,2LL)+2)%mod;
}

