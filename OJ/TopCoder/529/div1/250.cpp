class KingSort {
public:
	vector <string> getSortedList(vector <string>);
};

const string num[10] = {"","I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX"};
const string ten[5] = {"X","XX", "XXX", "XL", "L"};
string nn[55];
char a[100],b[100];
string sa,sb;

vector <string> KingSort::getSortedList(vector <string> kings) {
    for (int i = 1;i <= 50;i++)
    {
        if (i < 10)
            nn[i] = num[i];
        else
            nn[i] = ten[i/10-1]+num[i%10];
        cout << i << ' ' << nn[i] << endl;
    }
    int n = kings.size();
    vector<pair<string,int> > tmp;
    tmp.resize(n);
    for (int i = 0;i < n;i++)
    {
        sscanf(kings[i].c_str(),"%s%s",a,b);
        sa = a;
        sb = b;
        for (int j = 1;j <= 50;j++)
            if (sb == nn[j])
            {
                tmp[i].first = sa;
                tmp[i].second = j;
                break;
            }
    }
    sort(tmp.begin(),tmp.end());
    vector <string> res = kings;
    for (int i = 0;i < n;i++)
        res[i] = tmp[i].first+" "+nn[tmp[i].second];
    return res;
}

