class PouringWater {
public:
	int getMinBottles(int, int);
};

int PouringWater::getMinBottles(int N, int K) {
	int now,res = 0;
	for (now = 1;now < N;now <<= 1);
	for (int i = 0;i < K;i++)
	{
		while (now > N)
			now >>= 1;
		N -= now;
		cout << N << ' ' << now << endl;
		if (N == 0)	break;
		if (i+1 == K)
			if (N > 0)
				res = now-N;
	}
	return res;
}


